  Future<bool> checkAccountExistence(
      String phoneNumber, String userType) async {
    final querySnapshot = await _firebaseFirestore
        .collection(userType)
        .where('phone_number', isEqualTo: phoneNumber)
        .get();

    if (querySnapshot.docs.isNotEmpty) {
      return true;
    } else {
      return false;
    }
  }
